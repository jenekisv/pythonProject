r = int(input("Введите радиус круга: "))


def perimeter():  # Функция длины окружности
    return 2 * 3.141592 * r


print("Длина окружности:", round(perimeter(), 2))  # "Round" - Округляем до двух знаков после запятой


def area():  # Функция площади круга
    return 3.141592 * (r ** 2)


print("Площадь круга:", round(area(), 2))  # "Round" - Округляем до двух знаков после запятой
