# Глобальные переменные

isprint = False
x = input('Введите число 1: ')
y = input('Введите число 2: ')


def sum(x, y):  # Если функция не находит локальной переменной, внутри функции, то ищет глобальную.
    s = float(x) + float(y)
    if isprint:  # Определять её специально глобальной не надо.
        print(s)
    else:
        return s


print('Сумма равна: ', sum(x, y))

result = 0


def sub(x, y):
    global result  # Определять переменную "global" НАДО, так как глобальная = 0 и вычисления функции не будут учтены.
    result = float(x) - float(y)


sub(x, y)
print('Разность равна:', result)

